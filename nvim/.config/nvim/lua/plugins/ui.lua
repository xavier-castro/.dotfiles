 \     / /  /_\  \   Y   /  |       _/|       _/
 /     \/    |    \     /   |    |   \|    |   \
/___/\  \____|__  /\___/    |____|_  /|____|_  /
      \_/       \/                 \/        \/
   ]],
        },
      },
    },
  },
}
